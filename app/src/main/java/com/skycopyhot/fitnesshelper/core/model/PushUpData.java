public class PushUpData {
    private String name;
    private long start;
    private long end;
    private int count;
    private int type;

    public String getName() {
        return name;
    }

    public long getStartTime() {
        return start;
    }

    public long getEndTime() {
        return end;
    }

    public int getCount() {
        return count;
    }

    public int getType() {
        return type;
    }
}
